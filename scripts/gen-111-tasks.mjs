// Generates the shared /111 tasks.js from the playbook's authored phase data.
// Transforms each src/data/tasks/phaseNN.ts into a temp ESM module, imports it,
// and emits window.PLAYBOOK_PHASES + window.PLAYBOOK_TASKS.
import { readFileSync, writeFileSync, mkdirSync, rmSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { PROMPTS } from './prompts.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repo = join(__dirname, '..');
const tasksDir = join(repo, 'src', 'data', 'tasks');
const tmpDir = join(__dirname, '_tmp111');
mkdirSync(tmpDir, { recursive: true });

// Phase metadata (from src/data/phases.ts) + compact labels for the UI.
const PHASES = [
  { id: 1,  title: 'Idea & Validation',            short: 'Idea',      color: '#6366f1' },
  { id: 2,  title: 'Brand, Domain & Identity',     short: 'Brand',     color: '#ec4899' },
  { id: 3,  title: 'Foundation & Infrastructure',  short: 'Infra',     color: '#06b6d4' },
  { id: 4,  title: 'MVP Build',                     short: 'MVP',       color: '#f59e0b' },
  { id: 5,  title: 'Design & Quality Bar',         short: 'Design',    color: '#8b5cf6' },
  { id: 6,  title: 'Auth, Data & Security',        short: 'Security',  color: '#10b981' },
  { id: 7,  title: 'Content, SEO & Launch Prep',   short: 'SEO',       color: '#3b82f6' },
  { id: 8,  title: 'Analytics, Tracking & Feedback',short: 'Analytics',color: '#14b8a6' },
  { id: 9,  title: 'Email, Community & Retention',  short: 'Retention', color: '#f43f5e' },
  { id: 10, title: 'Growth to 10,000',             short: 'Growth',    color: '#f97316' },
  { id: 11, title: 'Fundraising: Idea to VC',      short: 'Fundraise', color: '#eab308' },
];

const all = [];
for (let i = 1; i <= 11; i++) {
  const nn = String(i).padStart(2, '0');
  const src = readFileSync(join(tasksDir, `phase${nn}.ts`), 'utf8');
  const js = src
    .replace(/^import[^\n]*\n/m, '')
    .replace(/export const phase\d+\s*:\s*Task\[\]\s*=/, 'export default');
  const tmp = join(tmpDir, `phase${nn}.mjs`);
  writeFileSync(tmp, js);
  const mod = await import('file://' + tmp.replace(/\\/g, '/'));
  for (const t of mod.default) {
    const row = { n: t.id, phase: t.phase, name: t.title, what: t.goal };
    const prompt = PROMPTS[t.id];
    if (prompt && prompt.trim()) row.prompt = prompt.trim();
    all.push(row);
  }
}

all.sort((a, b) => a.n - b.n);

// Compute each phase's task-id range.
const phases = PHASES.map((p) => {
  const ids = all.filter((t) => t.phase === p.id).map((t) => t.n);
  return { ...p, range: [Math.min(...ids), Math.max(...ids)], count: ids.length };
});

const header = `/*
 * The 111 Playbook (111.pete.ke) - canonical task list.
 * 111 tasks from idea to 10,000 users and a first VC cheque, across 11 phases.
 * Shared across every property's /111 checklist page - each site supplies its
 * own window.SITE + window.ANSWERS; this file is identical everywhere.
 * Auto-generated by scripts/gen-111-tasks.mjs - do not edit by hand.
 */\n`;

const out =
  header +
  'window.PLAYBOOK_PHASES = ' + JSON.stringify(phases) + ';\n' +
  'window.PLAYBOOK_TASKS = [\n' +
  all.map((t) => '  ' + JSON.stringify(t)).join(',\n') +
  '\n];\n';

const target = join(repo, 'scripts', '_out_111_tasks.js');
writeFileSync(target, out);
rmSync(tmpDir, { recursive: true, force: true });

console.log('phases:', phases.map((p) => `${p.id}:${p.count}[${p.range[0]}-${p.range[1]}]`).join(' '));
console.log('total tasks:', all.length);
console.log('with prompts:', all.filter((t) => t.prompt).length, '/ 111');
console.log('wrote:', target, '(' + out.length + ' bytes)');
