// Convert the generated task infographics (JPEG/PNG in _infographic_tmp/) into
// optimised WebP in public/images/tasks/. The pro image model returns JPEG, but
// sharp sniffs the real format regardless of the .png extension.
import sharp from 'sharp';
import { readdir, mkdir, writeFile } from 'node:fs/promises';
import { join, parse } from 'node:path';

const SRC = '_infographic_tmp';
const OUT = 'public/images/tasks';
const MANIFEST = 'src/data/infographics.ts';

await mkdir(OUT, { recursive: true });
const files = (await readdir(SRC)).filter((f) => /\.(png|jpg|jpeg)$/i.test(f));

let ok = 0;
const ids = new Set();
for (const f of files) {
  const { name } = parse(f);
  const out = join(OUT, `${name}.webp`);
  try {
    await sharp(join(SRC, f))
      // Keep aspect ratio; cap width so files stay small but crisp.
      .resize({ width: 1280, withoutEnlargement: true })
      .webp({ quality: 82 })
      .toFile(out);
    ok += 1;
    const m = name.match(/task-0*(\d+)/);
    if (m) ids.add(Number(m[1]));
    console.log('  wrote', out);
  } catch (e) {
    console.warn('  FAILED', f, '-', e.message);
  }
}

// Merge with any WebP already present (so partial re-runs don't drop existing ids).
for (const f of (await readdir(OUT)).filter((f) => f.endsWith('.webp'))) {
  const m = f.match(/task-0*(\d+)/);
  if (m) ids.add(Number(m[1]));
}

const sorted = [...ids].sort((a, b) => a - b);
const manifest = `// AUTO-GENERATED by scripts/convert-infographics.mjs — do not edit by hand.
// Task ids that have a generated infographic in /images/tasks/.
export const infographicIds = new Set<number>([${sorted.join(', ')}]);
`;
await writeFile(MANIFEST, manifest, 'utf8');

console.log(`Done: ${ok}/${files.length} infographics converted -> ${OUT}`);
console.log(`Manifest: ${sorted.length} ids -> ${MANIFEST}`);
